const CACHE="lightfix-v4";

self.addEventListener("install",e=>{
  e.waitUntil(
    caches.open(CACHE).then(cache=>{
      return cache.addAll([
        "/",
        "/index.html",
        "/db.js",
        "/sync.js",
        "/ui.js"
      ]);
    })
  );
});

self.addEventListener("fetch",e=>{
  e.respondWith(
    caches.match(e.request).then(res=>{
      return res||fetch(e.request);
    })
  );
});
